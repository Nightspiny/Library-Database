--creare tabele

CREATE TABLE utilizatori (
    id_utilizator INT PRIMARY KEY AUTO_INCREMENT,
    cnp VARCHAR(13) NOT NULL UNIQUE,
    nume_utilizator VARCHAR(50),
    prenume_utilizator VARCHAR(50),
    telefon_utilizator VARCHAR(20),
    status_utilizator CHAR(1) DEFAULT 'N',
    CONSTRAINT chk_status_utilizator CHECK (status_utilizator IN ('Y', 'N'))
);

CREATE TABLE publicatii (
    id_publicatie INT PRIMARY KEY AUTO_INCREMENT,
    nume_publicatie VARCHAR(60),
    an_infiintare INT,
    website_publicatie VARCHAR(100),
    cif_publicatie VARCHAR(10) NOT NULL UNIQUE,
    CONSTRAINT chk_an_infiintare CHECK (an_infiintare BETWEEN 1609 AND 2030)
);

CREATE TABLE categorii (
    id_categorie INT PRIMARY KEY AUTO_INCREMENT,
    public_tinta VARCHAR(5),
    denumire_categorie VARCHAR(50) NOT NULL,
    tip_categorie VARCHAR(40),
    gen_literar VARCHAR(40),
    CONSTRAINT chk_public_tinta CHECK (public_tinta IN ('<3', '3-6', '+7', '10-16', '+18')),
    CONSTRAINT chk_tip_categorie CHECK (tip_categorie IN ('traducere', 'originala'))
);

CREATE TABLE carti (
    cod_isbn BIGINT PRIMARY KEY,
    titlu_carte VARCHAR(50) NOT NULL,
    disponibilitate_carte CHAR(1),
    nr_pagini INT NOT NULL,
    an_publicare INT NOT NULL,
    id_categorie INT NOT NULL,
    id_publicatie INT NOT NULL,
    FOREIGN KEY (id_categorie) REFERENCES categorii(id_categorie) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_publicatie) REFERENCES publicatii(id_publicatie) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT chk_disponibilitate_carte CHECK (disponibilitate_carte IN ('Y', 'N')),
    CONSTRAINT chk_nr_pagini CHECK (nr_pagini > 0)
);

CREATE TABLE autori (
    id_autor INT PRIMARY KEY AUTO_INCREMENT,
    nume_autor VARCHAR(60),
    nationalitate_autor VARCHAR(20),
    specializare_autor VARCHAR(30) NOT NULL
);

CREATE TABLE imprumuturi (
    id_utilizator INT NOT NULL,
    cod_isbn BIGINT NOT NULL,
    data_imprumut DATE NOT NULL,
    data_restituire DATE NOT NULL,
    PRIMARY KEY (id_utilizator, cod_isbn, data_imprumut),
    FOREIGN KEY (id_utilizator) REFERENCES utilizatori(id_utilizator) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (cod_isbn) REFERENCES carti(cod_isbn) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT chk_perioada_imprumut CHECK (DATEDIFF(data_restituire, data_imprumut) BETWEEN 0 AND 30)
);

CREATE TABLE colaborare (
    cod_isbn BIGINT NOT NULL,
    id_autor INT NOT NULL,
    data_colaborare DATE NOT NULL,
    sursa_colaborare VARCHAR(70),
    PRIMARY KEY (cod_isbn, id_autor),
    FOREIGN KEY (cod_isbn) REFERENCES carti(cod_isbn) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_autor) REFERENCES autori(id_autor) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE bibliotecari (
    id_bibliotecar INT PRIMARY KEY AUTO_INCREMENT,
    salariu_bibliotecar DECIMAL(6, 1) NOT NULL,
    telefon_bibliotecar VARCHAR(12),
    CONSTRAINT chk_salariu_bibliotecar CHECK (salariu_bibliotecar > 0)
);

CREATE TABLE depozite (
    id_depozit INT PRIMARY KEY AUTO_INCREMENT,
    id_bibliotecar INT NOT NULL,
    tip_depozit VARCHAR(50),
    FOREIGN KEY (id_bibliotecar) REFERENCES bibliotecari(id_bibliotecar) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE stocuri (
    id_stoc INT PRIMARY KEY AUTO_INCREMENT,
    id_depozit INT NOT NULL,
    cod_carte BIGINT NOT NULL,
    nr_exemplare INT NOT NULL,
    FOREIGN KEY (id_depozit) REFERENCES depozite(id_depozit) ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (cod_carte) REFERENCES carti(cod_isbn) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT chk_nr_exemplare CHECK (nr_exemplare >= 0)
);



